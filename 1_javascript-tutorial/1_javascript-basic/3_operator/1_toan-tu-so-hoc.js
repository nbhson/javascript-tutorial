/**
    + -> cộng
    - -> trừ
    * -> nhân
    ** -> lũy thừa
    / -> chia
    % -> chia lấy số dư
    ++ -> tăng 1 giá trị
    -- -> giảm 1 giá trị
 */

var a = 1, b = '5'
var c = a + b; 
var d = a - b; 
var e = a * b; 
console.log(c); // 15
console.log(d); // -4
console.log(e); // 5

